metro=float(input('Digite um valor em metros para saber a conversão em centímetro e milímetro: '))
print('Você digitou {} metros'.format(metro), end=' >>> ')
print('{} metros em centímetros é {} centímetros'.format(metro, metro*1000), end=' >>> ')
print('{} metros em milímetros é {} milímetros'.format(metro, metro*10000), end=' >>> ')